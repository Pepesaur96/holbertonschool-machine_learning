#!/usr/bin/env python3
"""Module for the function np_shape(matrix)."""
import numpy as np


def np_shape(matrix):
    """
    Function that calculates the shape of a numpy.ndarray.
    Args:
        - matrix: a numpy.ndarray to calculate the shape of.
    Returns:
        A tuple of integers, the shape of the matrix.
    """
    return matrix.shape
